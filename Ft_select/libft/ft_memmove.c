#include "libft.h"

void		*ft_memmove(void *dest, const void *src, size_t n)
{
	void	*tmp;

	tmp = (char *)malloc(sizeof(src) * n);
	ft_memcpy(tmp, src, n);
	ft_memcpy(dest, tmp, n);
	ft_memdel(&tmp);
	return (dest);
}
