#include "libft.h"

char		*ft_strcat(char *dest, const char *src)
{
	int	a;
	int	b;

	a = 0;
	b = ft_strlen(dest);
	while (src[a] != '\0')
	{
		dest[b] = src[a];
		a++;
		b++;
	}
	dest[b] = '\0';
	return (dest);
}
