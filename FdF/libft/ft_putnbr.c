#include "libft.h"

void		ft_putnbr(int n)
{
	int		a;

	a = 0;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		a = 1;
	}
	if (a == 0)
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = n * -1;
		}
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
			ft_putchar(48 + n);
	}
}
